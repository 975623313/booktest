package com.an.pojo;



public class Books {


	private int bookId;
	
	private String bookISBN;
	private String bookName;
	private String bookWriter;
	private String bookClass;
	private String bookDate;
	private String bookPublic;
	private String bookTopic;
	private int bookNumber;
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public String getBookISBN() {
		return bookISBN;
	}
	public void setBookISBN(String bookISBN) {
		this.bookISBN = bookISBN;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookWriter() {
		return bookWriter;
	}
	public void setBookWriter(String bookWriter) {
		this.bookWriter = bookWriter;
	}
	public String getBookClass() {
		return bookClass;
	}
	public void setBookClass(String bookClass) {
		this.bookClass = bookClass;
	}
	public String getBookDate() {
		return bookDate;
	}
	public void setBookDate(String bookDate) {
		this.bookDate = bookDate;
	}
	public String getBookPublic() {
		return bookPublic;
	}
	public void setBookPublic(String bookPublic) {
		this.bookPublic = bookPublic;
	}
	public String getBookTopic() {
		return bookTopic;
	}
	public void setBookTopic(String bookTopic) {
		this.bookTopic = bookTopic;
	}
	public int getBookNumber() {
		return bookNumber;
	}
	public void setBookNumber(int bookState) {
		this.bookNumber = bookState;
	}
	
	
}
