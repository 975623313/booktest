package com.an.pojo;

public class Overdues {

	private int overdueId;
	private String bookName;
	private String readerName;
	private int overdueDate;
	public int getOverdueId() {
		return overdueId;
	}
	public void setOverdueId(int overdueId) {
		this.overdueId = overdueId;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getReaderName() {
		return readerName;
	}
	public void setReaderName(String readerName) {
		this.readerName = readerName;
	}
	public int getOverdueDate() {
		return overdueDate;
	}
	public void setOverdueDate(int overdueDate) {
		this.overdueDate = overdueDate;
	}
}
